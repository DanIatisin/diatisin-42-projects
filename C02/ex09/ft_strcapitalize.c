char	*ft_strcapitalize(char *str)
{
	char	*start;

	start = str;
	while (*str != '\0')
	{
		if (str == start
			|| (!(*(str - 1) >= '0' && *(str - 1) <= '9')
				&& !(*(str - 1) >= 'A' && *(str - 1) <= 'Z')
				&& !(*(str -1) >= 'a' && *(str - 1) <= 'z')))
		{
			if (*str >= 'a' && *str <= 'z')
				*str -= 32;
		}
		else
		{
			if (*str >= 'A' && *str <= 'Z')
				*str += 32;
		}
		str++;
	}
	return (start);
}
